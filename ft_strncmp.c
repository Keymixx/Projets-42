#include "libft.h"

int ft_strncmp (char *s1, char *s2, int len)
{
    int i = 0;

    while(s1[i] && s2[i] && i < len)
    {
        if (s1[i] != s2[i])
            return(s1[i] - s2[i]);
        i++;
    }
    if (i == len)
        return (0);
    return(s1[i] - s2[i]);
}