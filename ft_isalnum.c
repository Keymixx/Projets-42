#include "libft.h"

int ft_isalnum (int nb)
{
    if (nb >= 64 && nb <= 90 || nb >= 97 && nb <= 122)
        return 1;
    if (nb >= 48 && nb <= 57)
        return 1;
    else
        return 0;
}